// ── Dev admin provisioning ────────────────────────────────────────────────────

/// Idempotent: grants the caller full admin access to the first seeded org.
///
/// Call once after connecting, or set NEXT_PUBLIC_DEV_ADMIN=true in the
/// frontend .env.local to have the client call it automatically on connect.
///
///   spacetime call lumiere-v1-j1uo0 ensure_dev_admin '{}'
#[spacetimedb::reducer]
pub fn ensure_dev_admin(ctx: &ReducerContext) -> Result<(), String> {
    let caller = ctx.sender();

    // Find the first org (seeded by seed_dev_data)
    let org = ctx
        .db
        .organization()
        .iter()
        .next()
        .ok_or("No org found — run seed_dev_data first")?;
    let org_id = org.id;

    let company = ctx
        .db
        .company()
        .iter()
        .find(|c| c.organization_id == org_id)
        .ok_or("No company found — run seed_dev_data first")?;
    let company_id = company.id;

    // Find or create owner role for this org
    let owner_role = ctx
        .db
        .role()
        .iter()
        .find(|r| r.organization_id == org_id && r.name == "owner")
        .unwrap_or_else(|| {
            ctx.db.role().insert(Role {
                id: 0,
                organization_id: org_id,
                name: "owner".to_string(),
                description: Some("Full access".to_string()),
                parent_id: None,
                permissions: vec!["*:*".to_string()],
                is_system: true,
                is_active: true,
                created_at: ctx.timestamp,
                updated_at: ctx.timestamp,
                metadata: None,
            })
        });

    // Ensure UserOrganization membership
    let already_member = ctx
        .db
        .user_organization()
        .iter()
        .any(|uo| uo.user_identity == caller && uo.organization_id == org_id && uo.is_active);

    if !already_member {
        ctx.db.user_organization().insert(UserOrganization {
            id: 0,
            user_identity: caller,
            organization_id: org_id,
            company_id: Some(company_id),
            role_id: owner_role.id,
            department_id: None,
            job_title: Some("Admin".to_string()),
            employee_id: None,
            date_joined: ctx.timestamp,
            is_active: true,
            is_default: true,
            metadata: None,
        });
        log::info!("[ensure_dev_admin] Added caller to org {}", org_id);
    }

    // Ensure UserRoleAssignment
    let already_assigned = ctx
        .db
        .user_role_assignment()
        .iter()
        .any(|a| a.user_identity == caller && a.role_id == owner_role.id && a.is_active);

    if !already_assigned {
        ctx.db.user_role_assignment().insert(UserRoleAssignment {
            id: 0,
            user_identity: caller,
            role_id: owner_role.id,
            organization_id: org_id,
            assigned_by: caller,
            assigned_at: ctx.timestamp,
            expires_at: None,
            is_active: true,
            metadata: None,
        });
    }

    // Ensure casbin policy: p, owner, <org_id>, *, * (full access)
    let org_id_str = org_id.to_string();
    let already_has_policy = ctx.db.casbin_rule().iter().any(|r| {
        r.ptype == "p" && r.v0.as_deref() == Some("owner") && r.v1.as_deref() == Some(&org_id_str)
    });

    if !already_has_policy {
        ctx.db.casbin_rule().insert(CasbinRule {
            id: 0,
            ptype: "p".to_string(),
            v0: Some("owner".to_string()), // role subject
            v1: Some(org_id_str.clone()),  // domain (org)
            v2: Some("*".to_string()),     // resource (wildcard)
            v3: Some("*".to_string()),     // action (wildcard)
            v4: Some("allow".to_string()), // effect
            v5: None,
            created_at: ctx.timestamp,
            metadata: None,
        });
    }

    // Ensure casbin grouping: g, <caller_hex>, owner, <org_id> (user→role mapping)
    let caller_hex = caller.to_hex().to_string();
    let already_grouped = ctx.db.casbin_rule().iter().any(|r| {
        r.ptype == "g"
            && r.v0.as_deref() == Some(&caller_hex)
            && r.v1.as_deref() == Some("owner")
            && r.v2.as_deref() == Some(&org_id_str)
    });

    if !already_grouped {
        ctx.db.casbin_rule().insert(CasbinRule {
            id: 0,
            ptype: "g".to_string(),
            v0: Some(caller_hex),          // user identity hex
            v1: Some("owner".to_string()), // role name
            v2: Some(org_id_str),          // domain (org)
            v3: None,
            v4: None,
            v5: None,
            created_at: ctx.timestamp,
            metadata: None,
        });
    }

    log::info!(
        "[ensure_dev_admin] Caller provisioned as owner of org {}",
        org_id
    );

    Ok(())
}
